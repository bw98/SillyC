struct TreeNode {
    int val;
    TreeNode *left;
    TreeNode *right;
    TreeNode(int x) : val(x), left(NULL), right(NULL) {}
};

count(TreeNode* root) {
    // 满二叉树的总结点数可以通过二叉树的高度直接算出来
    int h = 0;
    while (root) {
        ++h;
        root = root->left;
    }

    return std::pow(2, h) - 1;
}

